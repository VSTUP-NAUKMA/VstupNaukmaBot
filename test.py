# Припустимо, що 'text' - це ваша строка з коефіцієнтами, як вище.
def parse_coefficients(text):
    coefficients1 = {}
    # Розділіть текст на рядки та проітеруйте кожен з них
    for line in text.split('\n'):
        parts = line.split('\t')
        if len(parts) > 4:
            # Назва спеціальності знаходиться в другому стовпці
            specialty_name = parts[1].strip()
            # Виберіть коефіцієнти, ігноруючи творчий конкурс (припускаємо, що він останній)
            specialty_coefficients = parts[2:-1]
            coefficients1[specialty_name] = specialty_coefficients
    return coefficients1


